import fs from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const INPUT_CSS  = path.join(ROOT, 'libs/ui/theme/dist/theme-presets.css');
const OUTPUT_TS  = path.join(ROOT, 'libs/ui/theme/src/lib/theme-presets.registry.ts');

// 1. Lire le CSS brut
const rawCss = fs.readFileSync(INPUT_CSS, 'utf8');

// 2. EXTRACTION DES BLOCS
//
// On veut capturer chaque bloc complet:
//   .tch-theme[data-preset=violet-green] { ... }
//   .tch-theme.dark[data-preset="rose-blue"] { ... }
//
// Note: [^}] *non-greedy*, en "s" (dotall) via [\s\S]*?
//
// On capture le sélecteur complet séparément,
// puis le corps dans { ... }.
//
const blockRegex = /(\.tch-theme(?:\.dark)?\[data-preset=(?:\"[^\"]+\"|[^\]\s]+)\]\s*\{)([\s\S]*?)(\})/g;

// presetMap = { [id]: { lightBlock: string, darkBlock: string } }
const presetMap = new Map();

let match;
while ((match = blockRegex.exec(rawCss)) !== null) {
  const fullSelector = match[1]; // e.g. `.tch-theme.dark[data-preset=violet-green]{`
  const body         = match[2]; // inside { ... }
  const closingBrace = match[3]; // "}"

  const fullBlock = fullSelector + body + closingBrace;

  // 3. RÉCUPÉRER L'ID DU PRESET
  //
  // On supporte :
  //   [data-preset="violet-green"]
  //   [data-preset=violet-green]
  //
  // On capture jusqu'à ] ou espace, sans attraper la suite.
  //
  const idMatch = fullSelector.match(/\[data-preset=(?:"([^"]+)"|([^\]\s]+))/);
  if (!idMatch) {
    console.warn('⚠️ Impossible de récupérer presetId dans le sélecteur:', fullSelector);
    continue;
  }
  const presetId = idMatch[1] || idMatch[2]; // groupe 1 = quoted, groupe 2 = unquoted

  // 4. LIGHT ou DARK ?
  const isDark = /\.tch-theme\.dark/.test(fullSelector);

  if (!presetMap.has(presetId)) {
    presetMap.set(presetId, { lightBlock: '', darkBlock: '' });
  }
  const entry = presetMap.get(presetId);
  if (isDark) {
    entry.darkBlock += fullBlock + '\n';
  } else {
    entry.lightBlock += fullBlock + '\n';
  }
}

// 5. Construire le tableau final
const presetsArr = [];
for (const [id, { lightBlock, darkBlock }] of presetMap.entries()) {
  // Label humain ("violet-green" -> "Violet Green")
  const label = id
    .split('-')
    .map(part => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');

  const css = (lightBlock + '\n' + darkBlock).trim();

  presetsArr.push({ id, label, css });
}

// 6. Helper pour stringifier en template literal
function tsStringLiteral(str) {
  return '`' + str.replace(/`/g, '\\`') + '`';
}

// 7. Écrire le TS
let tsOut = `// AUTO-GENERATED by generate-theme-registry.mjs
// Do not edit manually.

import { TchTheme } from './theme.types';

export const THEME_PRESETS: TchTheme[] = [
`;

for (const p of presetsArr) {
  tsOut += `  {
    id: ${JSON.stringify(p.id)},
    label: ${JSON.stringify(p.label)},
    css: ${tsStringLiteral(p.css)},
  },\n`;
}

tsOut += `];
`;

fs.mkdirSync(path.dirname(OUTPUT_TS), { recursive: true });
fs.writeFileSync(OUTPUT_TS, tsOut, 'utf8');

console.log(`✅ Generated ${OUTPUT_TS} with ${presetsArr.length} preset(s).`);
