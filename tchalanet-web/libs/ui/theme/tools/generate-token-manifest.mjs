/* generate-token-manifest.mjs
   Extracts the canonical --tch-* token vocabulary from the theme SCSS sources of truth
   (runtime-root.scss + runtime-vars.scss) and writes a committed, reviewable manifest.

   Run: node libs/ui/theme/tools/generate-token-manifest.mjs   (npm run tokens:generate)

   The manifest is the single canonical list the docs and the token contract test reference.
   Never hand-edit the generated file — edit the SCSS and regenerate. */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const scssDir = resolve(here, '../src/scss');
const outFile = resolve(here, '../src/registry/token-manifest.generated.ts');

const DECL = /(--tch-[a-z0-9-]+)\s*:/g;

function declaredTokens(file) {
  const css = readFileSync(resolve(scssDir, file), 'utf8');
  const set = new Set();
  let m;
  while ((m = DECL.exec(css)) !== null) set.add(m[1]);
  return set;
}

const root = declaredTokens('runtime-root.scss');
const vars = declaredTokens('runtime-vars.scss');
const emitted = [...new Set([...root, ...vars])].sort();

const banner =
  '// GENERATED by libs/ui/theme/tools/generate-token-manifest.mjs — do not hand-edit.\n' +
  '// Source of truth: scss/runtime-root.scss (statics + first-paint fallback) + scss/runtime-vars.scss (mat-sys bridge).\n' +
  '// Regenerate with `npm run tokens:generate`. The token contract test fails if this file is stale.\n\n';

const body =
  'export const TCH_TOKEN_MANIFEST = [\n' +
  emitted.map((t) => `  '${t}',`).join('\n') +
  '\n] as const;\n\n' +
  'export type TchTokenName = (typeof TCH_TOKEN_MANIFEST)[number];\n';

writeFileSync(outFile, banner + body);
console.log(`token-manifest: wrote ${emitted.length} tokens to ${outFile}`);
