// Generates the Material 3 theme preset registry consumed at runtime.
//
// Source of truth: libs/shared/theme/src/scss/theme-presets.scss (the preset catalog).
// This script compiles it once, splits the output per `data-preset` id (light + dark blocks),
// and writes a reviewable TS registry. Run via: npm run theme:generate
//
// Do not hand-edit the generated registry; edit the catalog and regenerate.

import { execFileSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const appRoot = resolve(here, '..');
const repoRoot = resolve(appRoot, '..', '..');
const catalog = resolve(repoRoot, 'libs/shared/theme/src/scss/theme-presets.scss');
const outFile = resolve(appRoot, 'src/app/core/theme/theme-presets.registry.ts');

// `sass` is a pnpm transitive dep (not hoisted), so the JS API isn't importable here. The CLI shim
// in node_modules/.bin is available and reliable across the pnpm layout.
const sassBin = resolve(repoRoot, 'node_modules/.bin/sass');
const compiled = execFileSync(
  sassBin,
  [catalog, '--load-path', resolve(repoRoot, 'node_modules'), '--style=expanded', '--no-source-map'],
  { encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 },
);

/** Brace-match a CSS rule body starting at the index of its opening `{`. */
function matchBlock(css, openBraceIndex) {
  let depth = 0;
  for (let i = openBraceIndex; i < css.length; i++) {
    if (css[i] === '{') depth++;
    else if (css[i] === '}' && --depth === 0) return i;
  }
  throw new Error('Unbalanced braces in compiled theme CSS');
}

// Collect every `.tch-theme[...data-preset=ID]{...}` rule (light and dark), in source order,
// grouped by preset id.
const ruleHeader = /\.tch-theme(?:\.dark)?\[data-preset=([a-z0-9-]+)\][^{]*\{/g;
const byId = new Map();
let match;
while ((match = ruleHeader.exec(compiled)) !== null) {
  const id = match[1];
  const open = compiled.indexOf('{', match.index);
  const close = matchBlock(compiled, open);
  const rule = compiled.slice(match.index, close + 1).trim();
  if (!byId.has(id)) byId.set(id, []);
  byId.get(id).push(rule);
  ruleHeader.lastIndex = close + 1;
}

if (byId.size === 0) {
  throw new Error('No presets found — check the catalog SCSS');
}

const presets = [...byId.entries()].map(([id, rules]) => ({
  id,
  labelKey: `theme.presets.${id}`,
  css: rules.join('\n\n'),
}));

const banner = `// AUTO-GENERATED by tools/generate-theme-registry.mjs — do not edit by hand.\n// Source: libs/shared/theme/src/scss/theme-presets.scss. Regenerate via: npm run theme:generate\n`;
const body = `import { ThemePreset } from '../../shared/types';\n\nexport const THEME_PRESETS: readonly ThemePreset[] = ${JSON.stringify(
  presets,
  null,
  2,
)} as const;\n`;

writeFileSync(outFile, `${banner}\n${body}`);
console.log(`Wrote ${presets.length} presets to ${outFile}`);
